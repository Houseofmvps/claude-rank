/**
 * robots-analyzer.mjs — Analyze and generate robots.txt with AI bot awareness.
 * Tracks AI crawlers and generates AI-friendly robots.txt files.
 */

import fs from 'node:fs';
import path from 'node:path';

// ---------------------------------------------------------------------------
// Known AI bots to track
// ---------------------------------------------------------------------------

const AI_BOTS = [
  'GPTBot',
  'PerplexityBot',
  'ClaudeBot',
  'Claude-Web',
  'Google-Extended',
  'CCBot',
  'anthropic-ai',
  'Googlebot',
  'Bingbot',
];

// ---------------------------------------------------------------------------
// analyzeRobotsTxt
// ---------------------------------------------------------------------------

/**
 * Parse a robots.txt string and return structured analysis.
 * @param {string} content
 * @returns {{ blockedBots: string[], allowedBots: string[], hasSitemap: boolean, sitemapUrls: string[], rules: Array<{agent: string, allow: string[], disallow: string[]}> }}
 */
export function analyzeRobotsTxt(content) {
  const lines = content.split(/\r?\n/);

  const blockedBots = new Set();
  const allowedBots = new Set();
  const sitemapUrls = [];
  const rules = [];

  let currentAgents = [];
  let currentAllow = [];
  let currentDisallow = [];

  function flushBlock() {
    if (currentAgents.length === 0) return;

    for (const agent of currentAgents) {
      rules.push({ agent, allow: [...currentAllow], disallow: [...currentDisallow] });

      const matchedBot = AI_BOTS.find(
        (b) => b.toLowerCase() === agent.toLowerCase()
      );

      if (matchedBot) {
        const hasDisallow = currentDisallow.some((d) => d.trim() !== '');
        const hasAllow = currentAllow.some((a) => a.trim() !== '');

        if (hasDisallow && !hasAllow) {
          blockedBots.add(matchedBot);
        } else if (hasAllow) {
          allowedBots.add(matchedBot);
        }
      }
    }

    currentAgents = [];
    currentAllow = [];
    currentDisallow = [];
  }

  for (const rawLine of lines) {
    // Strip inline comments
    const line = rawLine.replace(/#.*$/, '').trim();

    if (line === '') {
      // Blank line = end of block
      flushBlock();
      continue;
    }

    const colonIdx = line.indexOf(':');
    if (colonIdx === -1) continue;

    const field = line.slice(0, colonIdx).trim().toLowerCase();
    const value = line.slice(colonIdx + 1).trim();

    if (field === 'user-agent') {
      // If we already have agents with rules, a new User-agent starts a NEW block
      // unless this is the first directive in the block (multiple User-agent lines stack)
      if (currentAllow.length > 0 || currentDisallow.length > 0) {
        flushBlock();
      }
      currentAgents.push(value);
    } else if (field === 'disallow') {
      currentDisallow.push(value);
    } else if (field === 'allow') {
      currentAllow.push(value);
    } else if (field === 'sitemap') {
      sitemapUrls.push(value);
    }
  }

  // Flush any remaining block
  flushBlock();

  return {
    blockedBots: [...blockedBots],
    allowedBots: [...allowedBots],
    hasSitemap: sitemapUrls.length > 0,
    sitemapUrls,
    rules,
  };
}

// ---------------------------------------------------------------------------
// generateRobotsTxt
// ---------------------------------------------------------------------------

/**
 * Generate an AI-friendly robots.txt string allowing all bots.
 * @param {string} baseUrl
 * @returns {string}
 */
export function generateRobotsTxt(baseUrl) {
  const base = baseUrl.replace(/\/$/, '');
  const sitemapUrl = `${base}/sitemap.xml`;

  const lines = [
    '# robots.txt — generated by claude-rank',
    '# AI-friendly: all major crawlers explicitly allowed',
    '',
    '# General',
    'User-agent: *',
    'Allow: /',
    '',
  ];

  for (const bot of AI_BOTS) {
    lines.push(`User-agent: ${bot}`);
    lines.push('Allow: /');
    lines.push('');
  }

  lines.push(`Sitemap: ${sitemapUrl}`);
  lines.push('');

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

if (process.argv[1] === new URL(import.meta.url).pathname) {
  const [,, command, dir, baseUrl] = process.argv;

  if (!command || !dir) {
    console.error('Usage:');
    console.error('  node tools/robots-analyzer.mjs analyze <dir>');
    console.error('  node tools/robots-analyzer.mjs generate <dir> <base-url>');
    process.exit(1);
  }

  const absDir = path.resolve(dir);

  if (command === 'analyze') {
    const robotsPath = path.join(absDir, 'robots.txt');
    if (!fs.existsSync(robotsPath)) {
      console.error(`No robots.txt found at ${robotsPath}`);
      process.exit(1);
    }
    const content = fs.readFileSync(robotsPath, 'utf8');
    const result = analyzeRobotsTxt(content);
    console.log(JSON.stringify(result, null, 2));
  } else if (command === 'generate') {
    if (!baseUrl) {
      console.error('generate requires <base-url>');
      process.exit(1);
    }
    const content = generateRobotsTxt(baseUrl);
    const outPath = path.join(absDir, 'robots.txt');
    fs.writeFileSync(outPath, content, 'utf8');
    console.log(`robots.txt written to ${outPath}`);
  } else {
    console.error(`Unknown command: ${command}`);
    process.exit(1);
  }
}
